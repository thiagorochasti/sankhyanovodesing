# PowerShell script para reiniciar o TypeScript Server no VS Code

Write-Host "================================================================" -ForegroundColor Cyan
Write-Host "  COMO REINICIAR O TYPESCRIPT SERVER NO VS CODE" -ForegroundColor Yellow
Write-Host "================================================================" -ForegroundColor Cyan
Write-Host ""
Write-Host "Siga estes passos DENTRO do VS Code:" -ForegroundColor Green
Write-Host ""
Write-Host "1. Pressione: Ctrl + Shift + P" -ForegroundColor White
Write-Host "   (Isso abre a Command Palette)" -ForegroundColor Gray
Write-Host ""
Write-Host "2. Digite: 'TypeScript: Restart TS Server'" -ForegroundColor White
Write-Host "   (Você pode digitar apenas 'restart ts' que já aparece)" -ForegroundColor Gray
Write-Host ""
Write-Host "3. Pressione Enter" -ForegroundColor White
Write-Host ""
Write-Host "================================================================" -ForegroundColor Cyan
Write-Host ""
Write-Host "Depois disso, os erros devem desaparecer!" -ForegroundColor Green
Write-Host ""
Write-Host "ALTERNATIVA:" -ForegroundColor Yellow
Write-Host "- Feche o VS Code completamente e abra novamente" -ForegroundColor White
Write-Host ""
